public class ColorValue
{
    public static void main(String[]args)
    {
        char color='r';
        switch(color)
        {
            case 'g':System.out.println("green");
            break;
             case 'b':System.out.println("blue");
            break;
             
            case 'r':System.out.println("red");
            break;
            
            default:System.out.println("It is not a color");
            
        }
    }
}